package Punto;

public class Punto {
    public int x; // Atributos de la clase
    public int y;

    public Punto() { // Constructor por defecto
        x = 0;
        y = 0;
    }
    public Punto(int coordX, int coordY) { // Constructor con parametros
        x = coordX;
        y = coordY;
    }
}
