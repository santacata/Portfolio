package personal;

public class Maquinista {
    public String nombre;
    public String dni;
    public double sueldo;
    public String rango;

    public Maquinista(String nombre, String dni, double sueldo, String rango) {
        this.nombre = nombre;
        this.dni = dni;
        this.sueldo = sueldo;
        this.rango = rango;
    }
}